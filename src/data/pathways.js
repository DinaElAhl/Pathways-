export function getPathway(slug) {
  return pathways.find((p) => p.slug === slug);
}

export function getRelatedPathways(slug) {
  const pathway = getPathway(slug);
  if (!pathway || !pathway.relatedPaths) return [];
  return pathway.relatedPaths.map(s => getPathway(s)).filter(Boolean);
}
